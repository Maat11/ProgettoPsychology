
public class Iv {
	private int idPaz;
	private String iv;
	
//COSTRUTTORE:
	public Iv(int idPaz, String iv) {
		this.idPaz = idPaz;
		this.iv = iv;
	}
	
//GETTERS:
	public int getIdPaz() {
		return idPaz;
	}
	public String getIv() {
		return iv;
	}

//SETTERS:
	public void setIdPaz(int idPaz) {
		this.idPaz = idPaz;
	}
	public void setIv(String iv) {
		this.iv = iv;
	}
	
}
